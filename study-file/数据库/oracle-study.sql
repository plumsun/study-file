--多表条件排序
SELECT
	DISTINCT *
FROM
	CL_BIZ_APPLY a
LEFT JOIN CL_BIZ_BILL b ON
	a.CONTAIN_CERT = b.CONTAIN_CERT
WHERE
	a.DE_CLARE_TYPE = 'W'
	AND a.DELETE_FLAG = '1'
ORDER BY
	a.UPDATE_TIME DESC
	
